    public void Agregar() {
        CrudAntPer Caa = new CrudAntPer();
        Caa.AgregarConsulta(inputNC.getText(), inputN.getText(), inputAP.getText(), inputAM.getText(), inputD.getText(), inputTC.getText(), inputFN.getText(), inputAI.getText(), inputCE.getText());
    }

    //metodo para eliminar datos
    public void Eliminar() {
        CrudAntPer Caa = new CrudAntPer();
        Caa.EliminarConsulta(inputID.getText());
    }

    //metodo para modificar datos
    public void Modificar() {
        CrudAntPer Caa = new CrudAntPer();
        //en el mismo orden que el metodo en operaciones
        Caa.ModificarConsulta(inputNC.getText(), inputN.getText(), inputAP.getText(), inputAM.getText(), inputFN.getText(), inputD.getText(), inputTC.getText(), inputAI.getText(), inputCE.getText(), inputID.getText());
    }

