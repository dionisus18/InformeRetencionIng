    private void Tabla1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tabla1MouseClicked
        DefaultTableModel model = (DefaultTableModel) this.Tabla1.getModel();
        int i = Tabla1.getSelectedRow();
//condicion si no se selecciono ninguno
        if (i == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione");
        } else {
//mostramso los datos en los jtxt
            //convertimos en string el resultado
            inputID.setText(String.valueOf(Tabla1.getValueAt(i, 0)));
            inputNC.setText(String.valueOf(Tabla1.getValueAt(i, 1)));
            inputN.setText(String.valueOf(Tabla1.getValueAt(i, 2)));
            inputAP.setText(String.valueOf(Tabla1.getValueAt(i, 3)));
            inputAM.setText(String.valueOf(Tabla1.getValueAt(i, 4)));
            inputD.setText(String.valueOf(Tabla1.getValueAt(i, 5)));
            inputTC.setText(String.valueOf(Tabla1.getValueAt(i, 6)));
            inputFN.setText(String.valueOf(Tabla1.getValueAt(i, 7)));
            inputAI.setText(String.valueOf(Tabla1.getValueAt(i, 8)));
            inputCE.setText(String.valueOf(Tabla1.getValueAt(i, 9)));
        }
    }//GEN-LAST:event_Tabla1MouseClicked

