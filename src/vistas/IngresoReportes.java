    private void tblReportMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblReportMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) this.tblReport.getModel();
        int i = tblReport.getSelectedRow();
        if (i == 1) {
            JOptionPane.showMessageDialog(this, "Seleccione");
        } else {
            txtIdReport.setText(String.valueOf(tblReport.getValueAt(i, 0)));
            txtFechaReport.setText(String.valueOf(tblReport.getValueAt(i, 1)));
            txtResponReport.setText(String.valueOf(tblReport.getValueAt(i, 2)));
            txtJefeArea.setText(String.valueOf(tblReport.getValueAt(i, 3)));
            txtAspecto.setText(String.valueOf(tblReport.getValueAt(i, 4)));
            txtJornada.setText(String.valueOf(tblReport.getValueAt(i, 5)));
        }
    }//GEN-LAST:event_tblReportMouseClicked

