    public void cargarAntecendete(String id) {
        CrudAntLab Cab = new CrudAntLab();
        String[] lista = new String[10];
        lista = Cab.CargarAntecedentes(id);
        this.inputID.setText(lista[0]);
        this.inputProTi.setText(lista[1]);
        this.inputLice.setText(lista[2]);
        this.inputDiplo.setText(lista[3]);
        this.inputMagi.setText(lista[4]);
        this.inputMBA.setText(lista[5]);
        this.inputDoc.setText(lista[6]);
        this.inputExpLab.setText(lista[7]);
        this.inputExpDo.setText(lista[8]);
        this.inputCur.setText(lista[9]);
        this.inputIDdoc.setText(lista[10]);
    }

    public void Agregar() {
        CrudAntLab Cab = new CrudAntLab();
        Cab.AgregarConsulta(inputProTi.getText(), inputLice.getText(), inputDiplo.getText(), inputMagi.getText(), inputMBA.getText(), inputDoc.getText(), inputExpLab.getText(), inputExpDo.getText(), inputCur.getText(), inputIDdoc.getText());
    }

    public void Eliminar() {
        CrudAntLab Cab = new CrudAntLab();
        Cab.EliminarConsulta(inputID.getText());
    }

    public void Modificar() {
        CrudAntLab Cab = new CrudAntLab();
        //en el mismo orden que el metodo en operaciones
        Cab.ModificarConsulta(inputProTi.getText(), inputLice.getText(), inputDiplo.getText(), inputMagi.getText(), inputMBA.getText(), inputDoc.getText(), inputExpLab.getText(), inputExpDo.getText(), inputCur.getText(), inputIDdoc.getText());
    }

