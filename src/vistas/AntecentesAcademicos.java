    public void cargarPeriodo() {
        CrudPeriodo cap = new CrudPeriodo();
        ArrayList<String> lista = new ArrayList<String>();
        lista = cap.CargarDatos();
        this.cmbPeriodo.removeAllItems();
        this.cmbPeriodo.addItem("Seleccione");
        for (String item : lista) {

            this.cmbPeriodo.addItem(item);
        }

    }

    public void Agregar() {
        CrudAntAca cap = new CrudAntAca();
        cap.AgregarConsulta(cmbPeriodo.getSelectedItem().toString(),
                 this.inputR.getText(), this.inputA.getText(), this.lblIDdoc.getText());

    }

    public void modificar() {
        CrudAntAca cap = new CrudAntAca();
        cap.ModificarConsulta(this.inputID.getText(), cmbPeriodo.getSelectedItem().toString(),
                 this.inputR.getText(), this.inputA.getText());
    }

    public void eliminar() {
        CrudAntAca cap = new CrudAntAca();
        cap.EliminarConsulta(this.inputID.getText());
    }

    public void DatosTabla() {
        CrudAntAca Caa = new CrudAntAca();
        DefaultTableModel model = new DefaultTableModel();
        model = Caa.lista(this.lblIDdoc.getText());
        this.jTable1.setModel(model);
    }

