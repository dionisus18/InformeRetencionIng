    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        int i = jTable1.getSelectedRow();
//condicion si no se selecciono ninguno
        if (i == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione");
        } else {
//mostramso los datos en los jtxt
            //convertimos en string el resultado
            this.inputID.setText(String.valueOf(jTable1.getValueAt(i, 0)));
            this.inputR.setText(String.valueOf(jTable1.getValueAt(i, 1)));
            this.inputA.setText(String.valueOf(jTable1.getValueAt(i, 2)));
            this.lblIDdoc.setText(String.valueOf(jTable1.getValueAt(i, 3)));
            this.cmbPeriodo.setSelectedItem(String.valueOf(jTable1.getValueAt(i, 4)));

        }
    }//GEN-LAST:event_jTable1MouseClicked

